#' ChronochRt
#'
#' ChronochRt offers an easy way to draw chronological charts from tables. It aims to provide an intuitive environment for anyone new to R.
#'
#' @section Features:
#' \itemize{\item Slim and intuitive chronological data set \item Import tabular
#' data files and \item Import Excel files (requires the package
#' \pkg{readxl} \item Possibility to display up to 2
#' chronological systems within the same region (e.g. long and short
#' chronologies) \item Layout of the chronological chart optimised for easy
#' readability and comprehensibility \item Years in BCE must be negative -
#' that's all you need to care about dates \item Handling of unsecured dates
#' \item Handling of gaps, e.g. abandonment phases of sites \item Optional text
#' labels \item Export of the chronological chart in various file formats
#' (raster and vector graphics) \item Easy customisation of the chronological
#' chart \item Based on the \href{https://www.tidyverse.org/}{tidyverse}: Seamless
#' integration in pipes, enhanced customsation with \pkg{ggplot2}}
#'
#' @section Getting started:
#' \itemize{ \item \href{https://gitlab.com/roset/chronochrt/-/raw/master/inst/ChronochRt_Cheatsheet.pdf?inline=false}{Cheatsheet}
#' \item \href{../doc/ChronochRt.html}{Vignette}
#' }
#'
#'
